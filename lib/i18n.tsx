type DictKeys = keyof typeof dict.en;

type I18nCtx = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: DictKeys, vars?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nCtx | null>(null);

function template(text: string, vars?: Record<string, string | number>) {
  if (!vars) return text;
  return Object.entries(vars).reduce((acc, [k, v]) => acc.replaceAll(`{${k}}`, String(v)), text);
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<Locale>(() => {
    if (typeof window === "undefined") return "ms";
    const saved = window.localStorage.getItem(STORAGE_KEY) as Locale | null;
    return saved && (saved === "en" || saved === "ms" || saved === "zh") ? saved : "ms";
  });

  useEffect(() => {
    window.localStorage.setItem(STORAGE_KEY, locale);
  }, [locale]);

  const value = useMemo<I18nCtx>(
    () => ({
      locale,
      setLocale,
      t: (key, vars) => template(dict[locale][key] ?? dict.en[key], vars),
    }),
    [locale],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used in I18nProvider");
  return ctx;
}
